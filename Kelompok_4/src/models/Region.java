package models;

/**
 *
 * @author Rory
 */
public class Region {
    private int id;         // DB -> REGION_ID
    private String name;    // DB -> REGION_NAME
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Region{" + "id=" + id + ", name=" + name + '}';
    }
    
}
